package com.urdomain.lecture.ch06;

public class Calculator {
	
	
	public void powerOn() {
		System.out.println("전원이 켜졌습니다.");
		
	}
	
	public int plus(int a , int b) {
		int result = a + b ;
		
		
		return result;
		
		
	}
	
	public double divide(int a , int b) {
		
		
		double result = a + b;
		
		return result;
		
	}
	
	void powerOff() {
		
		System.out.println("전원을 끕니다");
		
	}
	
	

}
