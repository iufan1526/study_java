package com.urdomain.lecture.ch03;

public class AssignmentOperatorExample {

	public static void main(String[] args) {
		
		int result1 = 0;
		
		result1 += 10;
		System.out.println(result1);
		
		result1 -= 4;
		System.out.println(result1);
		
		result1 /= 10;
		System.out.println(result1);
		
		result1 *= 10;
		System.out.println(result1);
		
		result1 %= 10;
		System.out.println(result1);
		
		
	}

}
